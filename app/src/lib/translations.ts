// Helper para obter tradução
export function getTranslation(
  obj: Record<string, TranslatableContent>, 
  key: string, 
  lang: string
): string {
  const item = obj[key];
  if (!item) return '';
  return item[lang as keyof TranslatableContent] || item.pt || '';
}
